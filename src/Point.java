public class Point {
	private int x;
	private int y;
	private int z;

	public void setCoord(int nx, int ny, int nz) {
		this.x = nx;
		this.y = ny;
		this.z = nz;
	}

	public int getZ() {
		return z;
	}

	public void setZ(int z) {
		this.z = z;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;

	}
}